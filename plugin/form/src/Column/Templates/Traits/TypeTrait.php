<?php

namespace YjHyperfAdminPligin\Form\Column\Templates\Traits;

trait TypeTrait
{
    protected string $type;

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type): void
    {
        $this->type = $type;
    }
}